package abstraction;

public abstract class Numbers {
	public abstract int nextPrime(int num);
	
	public abstract boolean isPrime(int num);
	
	public abstract int reverseNumber(int num);
	
	public abstract boolean petersonNumber(int num);
	
	public abstract boolean sunnyNumber(int num);
	
	public abstract boolean techNumber(int num);
	
	public abstract boolean neonNumber(int num);
	
	public abstract boolean spyNumber(int num);
	
	public abstract boolean emirp(int num);
}
